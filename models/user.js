const mongoose = require('mongoose');

//schema

const userSchema = new mongoose.Schema({
    username:{
        type:String,
        required:true,
        unique:true
    },
    email:{
        type:String,
        required:true,
        unique:true
    },
    password:{
        type:String,
        required:true,
        unique:true
    }
})



//model

const User = mongoose.model('User',userSchema);
module.exports=User;